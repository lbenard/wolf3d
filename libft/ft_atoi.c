#include "libft.h"

int		ft_atoi(const char *str)
{
	int		nb;
	char	*s;
	int		sign;

	s = (char*)str;
	while (*s == ' ' || *s == '\t' || *s == '\n' || *s == '\v' || *s == '\f' ||
			*s == '\r')
		s++;
	sign = (*s == '-') ? -1 : 1;
	if (*s == '-' || *s == '+')
		s++;
	nb = 0;
	while (*s >= '0' && *s <= '9')
	{
		nb = nb * 10 + *s - '0';
		s++;
	}
	return (nb * sign);
}
