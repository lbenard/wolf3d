#include "engine/blend.h"

void	blend_add(t_rgba *const a, const t_rgba *const b)
{
	a->r = (255 - b->a) * a->r + b->r * (b->a / 255.0f);
	a->g = (255 - b->a) * a->g + b->g * (b->a / 255.0f);
	a->b = (255 - b->a) * a->b + b->b * (b->a / 255.0f);
}